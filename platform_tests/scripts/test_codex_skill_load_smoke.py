def test_generator_renders_bom_source_frontmatter_first(tmp_path: Path) -> None:
    module = _load_module("generate_codex_skill_adapters_bom_smoke", GENERATOR_PATH)
    source_text = "\ufeff" + _valid_source()
    _write_canonical_skill(tmp_path, source_text)
    _write_registry(tmp_path, _source_hash(source_text))

    module.generate(tmp_path)

    adapter = (tmp_path / ".codex" / "skills" / "review" / "SKILL.md").read_text(encoding="utf-8")
    assert adapter.startswith("---\nname: review\n")
    assert "GTKB-CODEX-SKILL-ADAPTER" in adapter


def test_harness_parity_detects_missing_codex_adapter_frontmatter(tmp_path: Path) -> None:
    module = _load_module("check_harness_parity_missing_smoke", PARITY_PATH)
    source_text = _valid_source()
    _write_canonical_skill(tmp_path, source_text)
    _write_registry(tmp_path, _source_hash(source_text))
    _write_codex_adapter(tmp_path, "name: review\ndescription: Test skill.\n\n# review\n")

    report = module.check_harness_parity(tmp_path, harness="codex", role="loyal-opposition")

    assert report.overall_status == "FAIL"
    assert report.results[0].state == "MISSING"
    assert "missing opening YAML frontmatter delimiter" in report.results[0].note


