def test_skill_load_smoke_passes_on_valid_codex_adapter(tmp_path: Path) -> None:
    parity = _load_module("check_harness_parity_valid_smoke", PARITY_PATH)
    doctor = _load_module("doctor_codex_skill_valid_smoke", DOCTOR_PATH)
    source_text = _valid_source()
    _write_canonical_skill(tmp_path, source_text)
    _write_registry(tmp_path, _source_hash(source_text))
    _write_codex_adapter(tmp_path, _valid_adapter(source_text))

    report = parity.check_harness_parity(tmp_path, harness="codex", role="loyal-opposition")
    doctor_check = doctor._check_codex_skill_load_health(tmp_path)

    assert report.overall_status == "PASS"
    assert report.results[0].state == "PASS"
    assert doctor_check.status == "pass"
