_PHASE_TEMPLATE_BUILDERS = {
    "governance": _governance_templates,
    "infra": _infra_templates,
    "ai_components": _ai_component_templates,
    "compliance": _compliance_templates,
}


def _generate_spec_data(config: SpecScaffoldConfig) -> list[dict[str, Any]]:
    """Materialize the list of spec data dicts to generate, per config."""
    phases = config.resolve_phases()
    specs: list[dict[str, Any]] = []
    for phase, enabled in phases.items():
        if not enabled:
            continue
        builder = _PHASE_TEMPLATE_BUILDERS.get(phase)
        if builder is None:
            continue
        specs.extend(builder())
    return specs


