def scaffold_specs(
    db: KnowledgeDB,
    config: SpecScaffoldConfig,
    *,
    dry_run: bool = True,
) -> ScaffoldReport:
    """Generate a starter set of specifications for the given project config.

    By default (``dry_run=True``) nothing is written to the database —
    the return value is a ScaffoldReport showing what WOULD be generated,
    including per-spec quality scores computed via ``db.score_spec_quality()``.

    When ``dry_run=False``, each generated spec is inserted into the database
    with ``authority='inferred'`` so an owner can later promote the spec to
    ``authority='stated'`` via ``db.update_spec(...)``.

    Pre-existing specs (matched by ``handle``) are skipped, not overwritten.
    """
    generated: list[dict[str, Any]] = []
    skipped: list[dict[str, Any]] = []
    quality_summary: dict[str, int] = {
        "gold": 0,
        "silver": 0,
        "bronze": 0,
        "needs-work": 0,
    }
    low_quality_warnings: list[dict[str, Any]] = []

    for spec_data in _generate_spec_data(config):
        handle = spec_data.get("handle")

        # Skip if a spec with this handle already exists.
        if handle:
            existing = db.list_specs(handle=handle)
            if existing:
                skipped.append(
                    {
                        "id": spec_data["id"],
                        "handle": handle,
                        "reason": f"Spec with handle {handle!r} already exists",
                    }
                )
                continue

        if dry_run:
            # Build a synthetic dict shape that ``score_spec_quality()``
            # understands. The scorer reads assertions from either
            # ``assertions_parsed`` or ``_assertions_parsed`` (see db.py:1086)
            # — populate BOTH so a dry-run spec with executable assertions
            # does not falsely trip the ``NO_ASSERTIONS`` flag.
            synthetic = {
                **spec_data,
                "version": 1,
                "assertions_parsed": spec_data.get("assertions") or [],
                "_assertions_parsed": spec_data.get("assertions") or [],
            }
            quality = db.score_spec_quality(synthetic)
            entry = {**spec_data, "quality": quality}
        else:
            # Apply mode: insert into DB with authority='inferred'.
            insert_kwargs: dict[str, Any] = {
                "id": spec_data["id"],
                "title": spec_data["title"],
                "status": spec_data.get("status", "specified"),
                "changed_by": config.changed_by,
                "change_reason": config.change_reason,
                "description": spec_data.get("description"),
                "section": spec_data.get("section"),
                "scope": spec_data.get("scope"),
                "handle": spec_data.get("handle"),
                "tags": spec_data.get("tags"),
                "assertions": spec_data.get("assertions"),
                "type": spec_data.get("type", "requirement"),
                "authority": "inferred",
                "testability": spec_data.get("testability"),
            }
            created = db.insert_spec(**insert_kwargs)
            # After insert, the spec dict from get_spec() has real parsed
            # fields — score directly off that.
            stored = db.get_spec(created["id"])
            quality = db.score_spec_quality(stored) if stored is not None else {}
            entry = {**(stored or spec_data), "quality": quality}

        tier = quality.get("tier", "needs-work")
        quality_summary[tier] = quality_summary.get(tier, 0) + 1
        if tier in ("bronze", "needs-work"):
            low_quality_warnings.append(
                {
                    "id": spec_data["id"],
                    "tier": tier,
                    "score": quality.get("overall"),
                }
            )
        generated.append(entry)

    return ScaffoldReport(
        generated=generated,
        skipped=skipped,
        quality_summary=quality_summary,
        low_quality_warnings=low_quality_warnings,
        dry_run=dry_run,
    )
