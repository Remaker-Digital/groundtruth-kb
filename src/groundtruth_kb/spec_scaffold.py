"""
GroundTruth KB — F6 Specification Scaffold Generator.

Generates a starter set of governance, infrastructure, AI-component, and
compliance specifications for a newly-scaffolded project. Produces either
a dry-run report (default) or commits specs to the KB with
``authority='inferred'`` so owners can review and promote to
``authority='stated'`` via ``db.update_spec()``.

Used by ``gt project init`` (optional via ``ScaffoldOptions.spec_scaffold``)
and by the standalone ``gt scaffold specs`` CLI command.

Phase A: governance + infrastructure templates (minimal profile).
Phase B: adds ai_components + compliance templates (full profile).

Copyright (c) 2026 Remaker Digital, a DBA of VanDusen & Palmeter, LLC. All rights reserved.
Licensed under AGPL-3.0-or-later.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import TYPE_CHECKING, Any

if TYPE_CHECKING:
    from groundtruth_kb.db import KnowledgeDB


# ---------------------------------------------------------------------------
# Config + report dataclasses
# ---------------------------------------------------------------------------


@dataclass(frozen=True)
class SpecScaffoldConfig:
    """Configuration for a spec scaffold run.

    Attributes:
        profile: Scaffold profile — 'minimal' (governance + infra) or
            'full' (adds ai_components + compliance).
        include_governance: Include governance templates (default True).
        include_infra: Include infrastructure templates (default True).
        include_ai_components: Include AI component templates
            (default: True when profile='full', False when 'minimal').
        include_compliance: Include compliance templates
            (default: True when profile='full', False when 'minimal').
        changed_by: Author attribution for ``insert_spec`` calls.
        change_reason: Change reason for ``insert_spec`` calls.
    """

    profile: str = "minimal"
    include_governance: bool = True
    include_infra: bool = True
    include_ai_components: bool | None = None
    include_compliance: bool | None = None
    changed_by: str = "scaffold-generator"
    change_reason: str = "Generated by F6 spec scaffold"

    def resolve_phases(self) -> dict[str, bool]:
        """Resolve effective phase inclusion based on profile + overrides."""
        full = self.profile == "full"
        return {
            "governance": self.include_governance,
            "infra": self.include_infra,
            "ai_components": (self.include_ai_components if self.include_ai_components is not None else full),
            "compliance": (self.include_compliance if self.include_compliance is not None else full),
        }


@dataclass(frozen=True)
class ScaffoldReport:
    """Output of a scaffold_specs() call.

    Attributes:
        generated: List of generated spec dicts (each includes a 'quality' key
            when quality scoring ran). In dry_run mode these are synthetic;
            in apply mode these are the freshly-inserted specs from db.
        skipped: List of {id, reason} for specs skipped due to pre-existing
            handles or other conflicts.
        quality_summary: Count of generated specs in each quality tier.
        low_quality_warnings: List of {id, tier, score} for specs scoring
            bronze or needs-work — surface to the owner during review.
        dry_run: True when the report came from a dry run (nothing written
            to the database).
    """

    generated: list[dict] = field(default_factory=list)
    skipped: list[dict] = field(default_factory=list)
    quality_summary: dict[str, int] = field(default_factory=dict)
    low_quality_warnings: list[dict] = field(default_factory=list)
    dry_run: bool = True


